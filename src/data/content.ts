export const IDLE_SLOT_COUNT = IDLE_SLOT_INITIAL;

/** Coin cost to add the next slot; `null` if already at IDLE_SLOT_MAX. */
export function nextIdleSlotUnlockCost(currentSlotCount: number): number | null {
  if (currentSlotCount >= IDLE_SLOT_MAX) return null;
  if (currentSlotCount < IDLE_SLOT_INITIAL) return null;
  const idx = currentSlotCount - IDLE_SLOT_INITIAL;
  const costs = [40, 95, 190, 380];
  return costs[idx] ?? null;
}
