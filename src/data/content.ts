const stationOrder: CraftStation[] = ['hand', 'campfire', 'workbench', 'forge', 'kitchen'];

export function defaultStations(): CraftStation[] {
  return ['hand'];
}

export function mergeStation(stations: CraftStation[], s: CraftStation): CraftStation[] {
  if (stations.includes(s)) return stations;
  const next = [...stations, s];
  next.sort((a, b) => stationOrder.indexOf(a) - stationOrder.indexOf(b));
  return next;
}

